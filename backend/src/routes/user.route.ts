import { Router } from 'express'
import { userController } from '../controllers'
import { auth, validate } from '@/middlewares'
import { userValidations } from '@/validations/auth.validation'

const userRouter = Router()

// User CRUD operations (protected routes)
userRouter.post('/', auth('manageUsers'), validate(userValidations.createUser), userController.createUser)

userRouter.get('/', auth('getUsers'), validate(userValidations.getAllUsers), userController.getAllUsers)

/**
 * @swagger
 * /users/search:
 *   get:
 *     summary: Search users (admin only)
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: query
 *         name: q
 *         required: true
 *         schema:
 *           type: string
 *           minLength: 1
 *         description: Search query
 *       - in: query
 *         name: page
 *         schema:
 *           type: integer
 *           minimum: 1
 *           default: 1
 *         description: Page number
 *       - in: query
 *         name: limit
 *         schema:
 *           type: integer
 *           minimum: 1
 *           maximum: 100
 *           default: 10
 *         description: Number of items per page
 *     responses:
 *       200:
 *         description: Search results retrieved successfully
 *         content:
 *           application/json:
 *             schema:
 *               allOf:
 *                 - $ref: '#/components/schemas/SuccessResponse'
 *                 - type: object
 *                   properties:
 *                     data:
 *                       type: array
 *                       items:
 *                         $ref: '#/components/schemas/User'
 *       401:
 *         description: Unauthorized
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       403:
 *         description: Forbidden - admin access required
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
userRouter.get('/search', auth('getUsers'), validate(userValidations.searchUsers), userController.searchUsers)

